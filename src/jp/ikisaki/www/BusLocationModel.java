package jp.ikisaki.www;

public class BusLocationModel {
	private String latitude = "";
	private String longitude = "";
	private String companyName = "";
	private String rosenName = "";
	private String destination = "";
	private String hour = "";
	private String minute = "";
	private String delayMinute = "";

	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getRosenName() {
		return rosenName;
	}
	public void setRosenName(String rosenName) {
		this.rosenName = rosenName;
	}
	public String getDestination() {
		return destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public String getHour() {
		return hour;
	}
	public void setHour(String hour) {
		this.hour = hour;
	}
	public String getMinute() {
		return minute;
	}
	public void setMinute(String minute) {
		this.minute = minute;
	}
	public String getDelayMinute() {
		return delayMinute;
	}
	public void setDelayMinute(String delayMinute) {
		this.delayMinute = delayMinute;
	}
}
