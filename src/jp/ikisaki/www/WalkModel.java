package jp.ikisaki.www;

public class WalkModel {

	private int startNorthInt = 0;
	private int startEastInt = 0;
	private int goalNorthInt = 0;
	private int goalEastInt = 0;

	public int getStartNorthInt() {
		return startNorthInt;
	}

	public void setStartNorthInt(int startNorthInt) {
		this.startNorthInt = startNorthInt;
	}

	public int getStartEastInt() {
		return startEastInt;
	}

	public void setStartEastInt(int startEastInt) {
		this.startEastInt = startEastInt;
	}

	public int getGoalNorthInt() {
		return goalNorthInt;
	}

	public void setGoalNorthInt(int goalNorthInt) {
		this.goalNorthInt = goalNorthInt;
	}

	public int getGoalEastInt() {
		return goalEastInt;
	}

	public void setGoalEastInt(int goalEastInt) {
		this.goalEastInt = goalEastInt;
	}

}
